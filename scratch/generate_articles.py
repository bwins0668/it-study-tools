def fill_template_vars(template_slot, company, name, boss, department, time, document, delay, subject):
    # Formats variables inside kanji, kana, and zh
    output = {}
    for key in ["kanji", "kana", "zh"]:
        val = template_slot[key]
        val = val.replace("{company}", company["kanji"])
        val = val.replace("{company_kana}", company["kana"])
        val = val.replace("{name}", name["kanji"])
        val = val.replace("{name_kana}", name["kana"])
        val = val.replace("{boss}", boss["kanji"])
        val = val.replace("{boss_kana}", boss["kana"])
        val = val.replace("{department}", department["kanji"])
        val = val.replace("{department_kana}", department["kana"])
        val = val.replace("{time}", time["kanji"])
        val = val.replace("{time_kana}", time["kana"])
        val = val.replace("{document}", document["kanji"])
        val = val.replace("{document_kana}", document["kana"])
        val = val.replace("{delay}", delay["kanji"])
        val = val.replace("{delay_kana}", delay["kana"])
        val = val.replace("{subject}", subject["kanji"])
        val = val.replace("{subject_kana}", subject["kana"])
        output[key] = val
    return output

